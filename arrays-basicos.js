// Criação de lista(array)
console.log('Trabalhando com listas!\n');

// Uma lista em js pode conter vários tipos de dados
const brics = ['brasil', 'russia', 'india', 'china', 'africa do sul'];
console.log('Lista inicial', brics, '\n');

// Adiciona item ao fim da lista
brics.push('outro país');
console.log('Após push', brics, '\n');

// Remove último item da lista
brics.pop();
console.log('Após pop', brics, '\n');

// Remove primeiro item da lista
brics.shift();
console.log('Após shift', brics, '\n');

// Adiciona item no início da lista
brics.unshift('brasil');
console.log('Após unshift', brics, '\n');

// Informa o tamanho da lista
console.log('Tamanho da lista:', brics.length, '\n');






